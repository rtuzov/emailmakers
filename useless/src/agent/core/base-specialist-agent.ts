import { v4 as uuidv4 } from 'uuid';
import { generateTraceId, delay } from '../utils/tracing-utils';
import { DEFAULT_RETRY_POLICY } from '../../config/constants';
import { getLogger } from '../../shared/logger';
import { Agent, run, createCustomSpan } from '@openai/agents';
import { toolRegistry } from './tool-registry';

/**
 * BaseSpecialistAgent ‚Äì shared functionality for Content/Design/Quality/Delivery specialists.
 * Provides:
 * ‚Ä¢ agent/trace initialisation
 * ‚Ä¢ OpenAI SDK native tracing
 * ‚Ä¢ comprehensive function visibility
 * ‚Ä¢ simple retry helper
 * Reduces duplication across specialist implementations.
 */
export class BaseSpecialistAgent {
  protected readonly agentId: string;
  protected readonly traceId: string;
  protected workflowId: string;
  protected agentType: string;
  protected functionExecutionHistory: Array<{
    sequence: number;
    name: string;
    duration: number;
    success: boolean;
    timestamp: string;
    error?: string;
  }> = [];
  protected handoffHistory: Array<{
    sequence: number;
    from: string;
    to: string;
    timestamp: string;
    success: boolean;
  }> = [];
  protected sequenceCounter: number = 0;
  
  /**
   * Proper instance of the OpenAI Agents SDK `Agent`.
   * Using the concrete type ensures we catch SDK-level typing errors at compile time.
   */
  protected readonly agent: Agent;
  protected readonly logger = getLogger({ agent: 'specialist', name: 'base-specialist' });

  protected constructor(name: string, instructions: string, tools: any[] = []) {
    this.agentId = uuidv4();
    this.traceId = generateTraceId();
    this.workflowId = `workflow-${Date.now()}`;
    this.agentType = name;

    // Create a fully-typed SDK Agent instance (complies with latest docs)
    this.agent = new Agent({
      name,
      instructions,
      model: process.env.USAGE_MODEL || 'gpt-4o-mini',
      tools,
    });

    // üîç –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –Ω–∞—Ç–∏–≤–Ω—É—é —Ç—Ä–∞—Å—Å–∏—Ä–æ–≤–∫—É OpenAI SDK
    this.initializeNativeTracing();
  }

  /**
   * üîç –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –Ω–∞—Ç–∏–≤–Ω–æ–π —Å–∏—Å—Ç–µ–º—ã —Ç—Ä–∞—Å—Å–∏—Ä–æ–≤–∫–∏ OpenAI SDK
   */
  private initializeNativeTracing(): void {
    console.log(`üîç [${this.agentType}] Native OpenAI SDK tracing initialized`);
    console.log(`ü§ñ Agent: ${this.agentType}`);
    console.log(`üÜî Agent ID: ${this.agentId}`);
    console.log(`üîó Trace ID: ${this.traceId}`);
    console.log(`üìä Workflow ID: ${this.workflowId}`);
    console.log(`‚è∞ Start Time: ${new Date().toISOString()}`);
  }

  /**
   * üéØ Execute operation with native OpenAI SDK tracing
   */
  protected async traced<T>(label: string, op: () => Promise<T>): Promise<T> {
    return this.tracedFunction(label, { operation: label }, op);
  }

  /**
   * üéØ Execute function with native OpenAI SDK tracing
   */
  protected async tracedFunction<T>(
    functionName: string,
    inputs: any,
    executor: () => Promise<T>
  ): Promise<T> {
    const sequence = ++this.sequenceCounter;
    const startTime = Date.now();
    
    // Simplified tracing - execute with comprehensive logging
    this.logger.info(`üöÄ [${this.agentType}] Executing ${functionName}`, {
      agentId: this.agentId,
      traceId: this.traceId,
      sequence,
      inputs: typeof inputs === 'object' ? JSON.stringify(inputs).slice(0, 200) : inputs
    });

    try {
      const result = await executor();
      const duration = Date.now() - startTime;
      
      // Record function execution in history
      this.functionExecutionHistory.push({
        sequence,
        name: functionName,
        duration,
        success: true,
        timestamp: new Date(startTime).toISOString()
      });
      
      this.logger.info(`‚úÖ [${this.agentType}] Completed ${functionName}`, {
        agentId: this.agentId,
        traceId: this.traceId,
        sequence,
        duration,
        success: true
      });

      return result;
    } catch (error) {
      const duration = Date.now() - startTime;
      const errorMessage = error instanceof Error ? error.message : String(error);
      
      // Record failed function execution in history
      this.functionExecutionHistory.push({
        sequence,
        name: functionName,
        duration,
        success: false,
        timestamp: new Date(startTime).toISOString(),
        error: errorMessage
      });
      
      this.logger.error(`‚ùå [${this.agentType}] Failed ${functionName}`, {
        agentId: this.agentId,
        traceId: this.traceId,
        sequence,
        duration,
        error: errorMessage
      });
      
      throw error;
    }
  }

  /**
   * ü§ñ Execute OpenAI Agent with comprehensive tracing
   */
  protected async executeOpenAIAgent<T>(
    prompt: string,
    context: Record<string, any> = {}
  ): Promise<T> {
    return this.tracedFunction(
      'openai-agent-execution',
      { prompt: prompt.slice(0, 200), context },
      async () => {
        console.log(`ü§ñ [${this.agentType}] Starting OpenAI Agent execution`);
        console.log(`üìù Prompt: ${prompt.slice(0, 300)}...`);

        const result = await run(this.agent, prompt);
        
        console.log(`ü§ñ [${this.agentType}] OpenAI Agent execution completed`);
        console.log(`üì§ Result: ${JSON.stringify(result.finalOutput).slice(0, 200)}...`);

        return result as T;
      }
    );
  }

  /**
   * üîÑ Generic retry wrapper with enhanced tracing
   */
  protected async executeWithRetry<T>(
    operation: () => Promise<T>,
    maxRetries: number = DEFAULT_RETRY_POLICY.max_retries,
    context: string = 'operation',
  ): Promise<T> {
    return this.tracedFunction(
      `retry-${context}`,
      { maxRetries, context },
      async () => {
        for (let attempt = 0; attempt <= maxRetries; attempt++) {
          try {
            console.log(`üîÑ [${this.agentType}] Retry attempt ${attempt + 1}/${maxRetries + 1} for ${context}`);
            
            const result = await operation();
            
            if (attempt > 0) {
              console.log(`‚úÖ [${this.agentType}] Retry succeeded on attempt ${attempt + 1} for ${context}`);
            }
            
            return result;
          } catch (err) {
            const last = attempt === maxRetries;
            if (last) {
              console.error(`‚ùå [${this.agentType}] All retry attempts failed for ${context}`, err);
              throw err;
            }
            
            const backoff = Math.min(DEFAULT_RETRY_POLICY.retry_delay_ms * Math.pow(2, attempt), 10000);
            console.warn(`‚ö†Ô∏è [${this.agentType}] Attempt ${attempt + 1} failed for ${context}, retrying in ${backoff}ms`, err);
            await delay(backoff);
          }
        }
        throw new Error('executeWithRetry: unreachable');
      }
    );
  }

  /**
   * üîß Execute tool with tracing
   */
  protected async executeTracedTool<T>(
    toolName: string,
    toolInputs: any,
    toolExecutor: () => Promise<T>
  ): Promise<T> {
    return this.tracedFunction(
      `tool-${toolName}`,
      { tool: toolName, inputs: toolInputs },
      async () => {
        console.log(`üîß [${this.agentType}] Executing tool: ${toolName}`);
        console.log(`üì• Tool inputs: ${JSON.stringify(toolInputs).slice(0, 200)}...`);

        const result = await toolExecutor();
        
        console.log(`üîß [${this.agentType}] Tool ${toolName} completed successfully`);
        console.log(`üì§ Tool result: ${JSON.stringify(result).slice(0, 200)}...`);

        return result;
      }
    );
  }

  /**
   * üîç Execute validation with tracing
   */
  protected async executeTracedValidation<T>(
    validationType: string,
    validationInputs: any,
    validator: () => Promise<T>
  ): Promise<T> {
    return this.tracedFunction(
      `validation-${validationType}`,
      { type: validationType, inputs: validationInputs },
      async () => {
        console.log(`üîç [${this.agentType}] Running validation: ${validationType}`);

        const result = await validator();
        
        console.log(`üîç [${this.agentType}] Validation ${validationType} completed`);

        return result;
      }
    );
  }

  /**
   * üîÑ Trace agent handoff to another agent with native OpenAI SDK
   */
  protected async traceHandoff(
    toAgent: string,
    handoffData: any,
    validationResults?: any
  ): Promise<string> {
    const sequence = ++this.sequenceCounter;
    const handoffId = `handoff-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;
    const timestamp = new Date().toISOString();
    
    // Use OpenAI SDK createCustomSpan for handoff tracing
    try {
      await createCustomSpan({
        data: {
          name: `handoff-${this.agentType}-to-${toAgent}`
        }
      });
      
      // Record handoff in history
      this.handoffHistory.push({
        sequence,
        from: this.agentType,
        to: toAgent,
        timestamp,
        success: true
      });
      
      console.log(`üîÑ [${sequence}] HANDOFF: ${this.agentType} ‚Üí ${toAgent}`);
      console.log(`   üÜî Handoff ID: ${handoffId}`);
      console.log(`   ‚è∞ Time: ${timestamp}`);
      
      if (handoffData && Object.keys(handoffData).length > 0) {
        console.log(`   üì¶ Data: ${JSON.stringify(handoffData).slice(0, 200)}...`);
      }
      
      return handoffId;
    } catch (error) {
      console.error('‚ùå Failed to create handoff span:', error);
      
      // Record failed handoff in history
      this.handoffHistory.push({
        sequence,
        from: this.agentType,
        to: toAgent,
        timestamp,
        success: false
      });
      
      return handoffId;
    }
  }

  /**
   * üéØ Execute agent function with comprehensive tracing
   */
  protected async executeTracedAgentFunction<T>(
    functionName: string,
    inputs: any,
    executor: () => Promise<T>,
    metadata?: Record<string, any>
  ): Promise<T> {
    return this.tracedFunction(
      functionName,
      inputs,
      async () => {
        console.log(`üéØ [${this.agentType}] Executing agent function: ${functionName}`);
        
        const result = await executor();
        
        console.log(`üéØ [${this.agentType}] Agent function ${functionName} completed`);
        
        return result;
      }
    );
  }

  /**
   * üìä Get current trace information
   */
  protected getCurrentTrace(): any {
    const totalFunctions = this.functionExecutionHistory.length;
    const totalHandoffs = this.handoffHistory.length;
    const successfulFunctions = this.functionExecutionHistory.filter(f => f.success).length;
    const successRate = totalFunctions > 0 ? (successfulFunctions / totalFunctions) * 100 : 100;
    
    return {
      traceId: this.traceId,
      agentType: this.agentType,
      agentId: this.agentId,
      workflowId: this.workflowId,
      totalFunctions,
      totalHandoffs,
      successRate,
      functions: this.functionExecutionHistory,
      handoffs: this.handoffHistory
    };
  }

  /**
   * üèÅ Complete agent tracing
   */
  protected completeTracing(success: boolean, error?: string): any {
    const trace = this.getCurrentTrace();
    
    console.log(`üèÅ [${this.agentType}] Agent execution completed`);
    console.log(`üìä Functions executed: ${trace.totalFunctions}`);
    console.log(`üîÑ Handoffs performed: ${trace.totalHandoffs}`);
    console.log(`üìà Success rate: ${trace.successRate}%`);
    console.log(`‚úÖ Overall success: ${success}`);
    
    if (error) {
      console.log(`‚ùå Error: ${error}`);
    }
    
    return trace;
  }

  /**
   * üíæ Save trace to file (simplified native implementation)
   */
  public async saveTrace(filePath?: string): Promise<string | null> {
    try {
      const trace = this.getCurrentTrace();
      const fileName = filePath || `trace-${this.traceId}-${Date.now()}.json`;
      const fs = await import('fs');
      const path = await import('path');
      
      const fullPath = path.join(process.cwd(), 'logs', fileName);
      
      // Create directory if it doesn't exist
      const dir = path.dirname(fullPath);
      if (!fs.existsSync(dir)) {
        fs.mkdirSync(dir, { recursive: true });
      }
      
      await fs.promises.writeFile(fullPath, JSON.stringify(trace, null, 2));
      
      console.log(`üíæ [${this.agentType}] Trace saved to: ${fullPath}`);
      return fullPath;
    } catch (error) {
      console.error('‚ùå Failed to save trace:', error);
      return null;
    }
  }

  /**
   * üìã Print execution sequence
   */
  protected printExecutionSequence(): void {
    const trace = this.getCurrentTrace();
    console.log(`\nüìã [${this.agentType}] EXECUTION SEQUENCE:`);
    console.log(`üî¢ Total Functions: ${trace.totalFunctions}`);
    console.log(`üîÑ Total Handoffs: ${trace.totalHandoffs}`);
    console.log(`üìä Success Rate: ${trace.successRate}%`);
    
    // Print function history
    if (trace.functions.length > 0) {
      console.log(`\nüéØ Function Execution History:`);
      trace.functions.forEach((func: any, index: number) => {
        const status = func.success ? '‚úÖ' : '‚ùå';
        console.log(`  ${index + 1}. ${status} ${func.name} (${func.duration}ms)`);
        if (func.error) {
          console.log(`     ‚ùå Error: ${func.error}`);
        }
      });
    }
    
    // Print handoff history
    if (trace.handoffs.length > 0) {
      console.log(`\nüîÑ Handoff History:`);
      trace.handoffs.forEach((handoff: any, index: number) => {
        const status = handoff.success ? '‚úÖ' : '‚ùå';
        console.log(`  ${index + 1}. ${status} ${handoff.from} ‚Üí ${handoff.to}`);
      });
    }
  }

  /**
   * üìä Get agent status with tracing information
   */
  protected getAgentStatus() {
    const trace = this.getCurrentTrace();
    
    return {
      agentId: this.agentId,
      agentType: this.agentType,
      traceId: this.traceId,
      workflowId: this.workflowId,
      status: 'active',
      tracing: {
        functionsExecuted: trace.totalFunctions,
        handoffsPerformed: trace.totalHandoffs,
        successRate: trace.successRate
      },
      timestamp: new Date().toISOString()
    };
  }

  /**
   * üîç Get detailed function execution history
   */
  public getFunctionExecutionHistory(): any[] {
    return [...this.functionExecutionHistory];
  }

  /**
   * üîÑ Get handoff history
   */
  public getHandoffHistory(): any[] {
    return [...this.handoffHistory];
  }

  /**
   * üîß Get tools from Tool Registry for this agent type
   */
  protected getRegistryTools(agentType: 'content' | 'design' | 'quality' | 'delivery'): any[] {
    return toolRegistry.getToolsForAgent(agentType);
  }

  /**
   * üîß Get specific tool from Tool Registry
   */
  protected getRegistryTool(toolName: string): any {
    return toolRegistry.getOpenAITool(toolName);
  }

  /**
   * üìä Get Tool Registry statistics
   */
  protected getToolRegistryStats(): any {
    return toolRegistry.getToolStats();
  }

  /**
   * üîß Enable/disable tool in registry
   */
  protected setToolEnabled(toolName: string, enabled: boolean): boolean {
    return enabled ? toolRegistry.enableTool(toolName) : toolRegistry.disableTool(toolName);
  }
} 